#include <iostream>
#include <cmath>

using namespace std;

int main() {

	float a = sqrt(4);
	float b = sqrt(12.25);
	float c = sqrt(0.0121);

	cout << "Number\t\tSquare root" << endl;
	cout << "--------------------------------------------\n";
	cout << "4\t\t" << a << endl;
	cout << "12.25\t\t" << b << endl;
	cout << "0.0121\t\t" << c << endl;
}